     */
    protected $description = 'Generate Customer Settings Report';


    private $app;

    /**
     * Create a new command instance.
     *
     * @param Container $app
     */
    public function __construct(Container $app)
    {
        parent::__construct();

        $this->app = $app;
    }

    /**
     * Execute the console command.
     */
    public function handle()
    {
        exit;

        $companyPersonIds = $this->getPersonIds();
        $customerSettingsIds = $this->getCustomerSettingsIds($companyPersonIds);

        $this->updateSettings($customerSettingsIds);
        $this->updateItems($customerSettingsIds);
    }

    
    private function getPersonIds()
    {
        return DB::table('sl_records')
            ->whereIn('sl_record_id', $this->customerIds)
            ->where('table_name', 'person')
            ->pluck('record_id')
            ->all();
    }

    private function getCustomerSettingsIds(array $companyPersonIds)
    {
        return DB::table('customer_settings')
            ->whereIn('company_person_id', $companyPersonIds)
            ->pluck('customer_settings_id')
            ->all();
    }

    private function updateSettings(array $customerSettingsIds)
    {
        foreach ($customerSettingsIds as $customerSettingsId) {
            $customerSettings = CustomerSettings::where('customer_settings_id', $customerSettingsId)->first();
            $customerSettings->required_work_order_signature = 1;
            $customerSettings->save();
        }
    }
    
    private function updateItems(array $customerSettingsIds)
    {
        foreach ($customerSettingsIds as $customerSettingsId) {
            CustomerSettingsItem::updateOrCreate([
                'customer_settings_id' => $customerSettingsId,
                'key' => 'work_order.electronic_signature_accepted'
            ], [
                'value' => '1'
            ]);

            CustomerSettingsItem::updateOrCreate([
                'customer_settings_id' => $customerSettingsId,
                'key' => 'work_order.required_photo_of_invoice'
            ], [
                'value' => '0'
            ]);

            CustomerSettingsItem::updateOrCreate([
                'customer_settings_id' => $customerSettingsId,
                'key' => 'work_order.merge_electronic_signature_with_invoice'
            ], [
                'value' => '1'
            ]);
        }
    }

